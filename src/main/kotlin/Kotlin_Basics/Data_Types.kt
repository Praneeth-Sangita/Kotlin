package Kotlin_Basics

fun main(){
    var num1 : Int = 10
    var num2 : Float = 12.25F //Float number should always end with 'F' else it throws an error
    var num3 : Double=12.25
    var num4 : Float=35E3F
    var num5 : Double=35E3 // E indicates the power of 10 -> here 35E3 means 35*1000
    var isCodingEasy : Boolean=true
    var char1 : Char='a'
    var str1 : String="Hello World!!!!!"
    println(num1)
    println(num2)
    println(num3)
    println(num4)
    println(num5)
    println(isCodingEasy)
    println(char1)
    println(str1)
}